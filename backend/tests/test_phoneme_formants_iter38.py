class TestPlausibleStable200:
    def test_full_success_shape(self, headers, plausible_i_wav):
        _grant(headers, "audio", True)
        r = _analyze(headers, plausible_i_wav, "i", "AmE")
        assert r.status_code == 200, r.text
        body = r.json()

        # Top-level scoring shape unchanged
        assert isinstance(body.get("composite_score"), (int, float))
        cefr = body.get("cefr") or {}
        assert cefr.get("band")
        assert cefr.get("label")
        per_formant = body.get("per_formant") or []
        assert per_formant, "per_formant list must be non-empty"
        for pf in per_formant:
            assert pf["name"] in {"F1", "F2", "F3"}
            assert isinstance(pf.get("measured"), (int, float))
            assert isinstance(pf.get("reference"), (int, float))
            assert isinstance(pf.get("score"), (int, float))

        # Dispersion source ties to formant_references.sd_source ('estimated_pooled')
        assert body.get("dispersion_source") == "estimated", (
            f"dispersion_source must be 'estimated' when SDs are pooled "
            f"estimates, got: {body.get('dispersion_source')}"
        )

        # Diagnostics shape
        d = body.get("diagnostics") or {}
        assert d, f"diagnostics missing: {body}"
        assert d.get("reliable") is True

        # nucleus_sd_hz — present, has at least one formant
        n_sd = d.get("nucleus_sd_hz") or {}
        assert n_sd, f"nucleus_sd_hz missing/empty: {d}"
        for k, v in n_sd.items():
            assert k in {"F1", "F2", "F3"}
            assert isinstance(v, (int, float))

        # nucleus_sd_thresholds_hz — exact FIX A2 values
        thr = d.get("nucleus_sd_thresholds_hz") or {}
        assert thr == {"F1": 25.0, "F2": 50.0, "F3": 70.0}, thr

        # plausibility_range_hz — per formant with ref/sd_used/sd_source/min/max
        pr = d.get("plausibility_range_hz") or {}
        assert set(pr.keys()) & {"F1", "F2", "F3"}, pr
        for name, rng in pr.items():
            for key in ("ref", "sd_used", "sd_source", "min", "max"):
                assert key in rng, f"{name} range missing {key}: {rng}"
            assert rng["min"] < rng["max"]
            # sd_source must be 'estimated_pooled' for Hillenbrand dataset rows
            assert rng["sd_source"] == "estimated_pooled", (
                f"{name}.sd_source expected 'estimated_pooled', got "
                f"{rng['sd_source']!r}"
            )

    def test_within_window_sd_below_thresholds(self, headers, plausible_i_wav):
        _grant(headers, "audio", True)
        r = _analyze(headers, plausible_i_wav, "i", "AmE")
        assert r.status_code == 200, r.text
        d = r.json()["diagnostics"]
        thr = d["nucleus_sd_thresholds_hz"]
        n_sd = d["nucleus_sd_hz"]
        for k, sd in n_sd.items():
            assert sd <= thr[k], (
                f"FIX A2 assertion: chosen window {k}_sd={sd} exceeded "
                f"threshold={thr[k]} — window should have been rejected."
            )


