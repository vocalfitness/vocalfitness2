class TestPerPhonemePlausibilityRange:
    def test_i_AmE_men_F1_range_is_narrow(self, headers, plausible_i_wav):
        """/i/ AmE men F1 range must be roughly 219..465 Hz (± 3 × pooled
        SD around Hillenbrand mean 342 Hz). This proves plausibility is
        per-phoneme, NOT the old global F1>900 rule."""
        _grant(headers, "audio", True)
        r = _analyze(headers, plausible_i_wav, "i", "AmE")
        assert r.status_code == 200, r.text
        pr = r.json()["diagnostics"]["plausibility_range_hz"]
        f1_min = pr["F1"]["min"]
        f1_max = pr["F1"]["max"]
        # Hillenbrand men mean 342, pooled SD 12% → ~41, ±3σ → ~219..465
        assert 200 <= f1_min <= 240, f"F1 min expected ~219, got {f1_min}"
        assert 450 <= f1_max <= 490, f"F1 max expected ~465, got {f1_max}"
        assert pr["F1"]["ref"] == 342

    def test_i_AmE_rejects_f1_900(self, headers):
        """A synthesized signal with F1~1000 F2~900 (well within the OLD
        global F1<900 tolerance if we bumped F1 to say 850 — but far from
        /i/) must be rejected because the per-phoneme range for /i/ AmE men
        is 219..465 Hz."""
        _grant(headers, "audio", True)
        wav = _synthesize_vowel_wav(1000.0, 900.0, 2100.0)
        r = _analyze(headers, wav, "i", "AmE")
        assert r.status_code == 422, (
            f"F1~1000 must be rejected as /i/ (per-phoneme range 219..465). "
            f"got {r.status_code}: {r.text[:400]}"
        )
        assert r.json()["detail"]["reason"] == "implausible"

    def test_different_phoneme_has_different_range(self, headers,
                                                    plausible_i_wav):
        """Range for /ɑ/ AmE (open back vowel, F1 mean ~768) must be much
        higher than for /i/ (F1 mean ~342). Even if we cannot get a 200 for
        an /ɑ/ recording from a synthesized /i/ WAV, the 422 detail STILL
        exposes the /ɑ/ plausibility_range_hz — which must differ."""
        _grant(headers, "audio", True)
        # /i/ AmE range
        r_i = _analyze(headers, plausible_i_wav, "i", "AmE")
        assert r_i.status_code == 200, r_i.text
        rng_i = r_i.json()["diagnostics"]["plausibility_range_hz"]["F1"]
        # /ɑ/ AmE range: whether it succeeds or 422s, we can extract the range.
        r_a = _analyze(headers, plausible_i_wav, "ɑ", "AmE")
        assert r_a.status_code in (200, 422), r_a.text
        if r_a.status_code == 200:
            rng_a = r_a.json()["diagnostics"]["plausibility_range_hz"]["F1"]
        else:
            rng_a = r_a.json()["detail"]["expert"]["plausibility_range_hz"]["F1"]
        assert rng_a["ref"] != rng_i["ref"], (
            f"Expected different reference means per phoneme: /i/={rng_i}, "
            f"/ɑ/={rng_a}"
        )
        assert rng_a["ref"] > rng_i["ref"], (
            f"/ɑ/ F1 mean should be higher than /i/ F1 mean, got "
            f"/ɑ/={rng_a['ref']} /i/={rng_i['ref']}"
        )


