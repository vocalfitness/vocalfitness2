class TestImplausibleReturns422StructuredDetail:
    def test_off_target_signal_as_i_returns_422(self, headers, implausible_wav):
        # /bad.wav/ is F1~1000 F2~900 — nowhere near /i/ AmE (F1 342 F2 2322).
        _grant(headers, "audio", True)
        r = _analyze(headers, implausible_wav, "i", "AmE")
        assert r.status_code == 422, r.text
        body = r.json()
        detail = body.get("detail")
        assert isinstance(detail, dict), (
            f"422 detail must be a structured OBJECT (not a string). Got: "
            f"{type(detail).__name__} — {detail!r}"
        )

        # required keys on the detail object
        for k in ("message", "reason", "offenders", "expert"):
            assert k in detail, f"detail.{k} missing: {detail}"

        # actionable Italian message (no jargon)
        msg = (detail.get("message") or "").lower()
        assert "affidabile" in msg or "silenzioso" in msg or "fermo" in msg, (
            f"detail.message must be actionable Italian, got: {msg!r}"
        )

        # reason MUST be 'implausible' for this off-target signal
        assert detail.get("reason") == "implausible", (
            f"reason must be 'implausible' for off-target F1~1000 measured "
            f"as /i/, got: {detail.get('reason')!r}"
        )

        # expert.attempts must show ALL 3 ceilings were retried, each plausible=False
        expert = detail.get("expert") or {}
        attempts = expert.get("attempts") or []
        assert len(attempts) >= 3, (
            f"expert.attempts must show 3 LPC-ceiling retries "
            f"(5500/5000/4500), got: {attempts}"
        )
        ceilings_seen = {a.get("ceiling_hz") for a in attempts
                        if isinstance(a, dict)}
        assert {5500, 5000, 4500}.issubset(ceilings_seen), (
            f"expert.attempts must cover 5500/5000/4500 Hz, got ceilings: "
            f"{ceilings_seen}"
        )
        # Every attempt with a measurement must be marked plausible: False
        for a in attempts:
            if isinstance(a, dict) and a.get("F1") is not None:
                assert a.get("plausible") is False, (
                    f"attempt at ceiling={a.get('ceiling_hz')} should be "
                    f"plausible=False for this off-target signal, got: {a}"
                )

        # expert.plausibility_range_hz must be present
        pr = expert.get("plausibility_range_hz") or {}
        assert pr, f"expert.plausibility_range_hz missing: {expert}"
        for name, rng in pr.items():
            for key in ("ref", "sd_used", "sd_source", "min", "max"):
                assert key in rng, f"{name} range missing {key}: {rng}"

        # No composite_score smuggled on 422
        assert "composite_score" not in body

    def test_422_expert_reports_measured_f1_beyond_range(
        self, headers, implausible_wav
    ):
        """expert.attempts must show measured F1 values that are outside the
        plausibility range for /i/ AmE (i.e. > 465 Hz)."""
        _grant(headers, "audio", True)
        r = _analyze(headers, implausible_wav, "i", "AmE")
        assert r.status_code == 422, r.text
        expert = r.json()["detail"]["expert"]
        f1_max = expert["plausibility_range_hz"]["F1"]["max"]
        # At least one attempt should have F1 > f1_max, proving the rejection
        # is driven by the per-phoneme range (not a global 900 Hz cutoff).
        offenders = [a for a in expert["attempts"]
                     if isinstance(a, dict) and a.get("F1")
                     and a["F1"] > f1_max]
        assert offenders, (
            f"No attempt shows F1 above the per-phoneme plausibility ceiling "
            f"F1.max={f1_max}: attempts={expert['attempts']}"
        )


