def _resonator(x: np.ndarray, f: float, bw: float, sr: int) -> np.ndarray:
    r = float(np.exp(-np.pi * bw / sr))
    theta = 2 * np.pi * f / sr
    a1 = -2 * r * float(np.cos(theta))
    a2 = r * r
    y = np.zeros_like(x)
    y1 = y2 = 0.0
    for i, v in enumerate(x):
        cur = v - a1 * y1 - a2 * y2
        y[i] = cur
        y2 = y1
        y1 = cur
    return y


def _synthesize_vowel_wav(f1: float, f2: float, f3: float,
                          f0: float = 120.0, dur: float = 1.0,
                          sr: int = 16000) -> bytes:
    n = int(sr * dur)
    t = np.arange(n) / sr
    # glottal impulse train
    src = np.zeros(n)
    period = int(round(sr / f0))
    src[::period] = 1.0
    y = _resonator(src, f1, 90.0, sr)
    y = _resonator(y, f2, 110.0, sr)
    y = _resonator(y, f3, 170.0, sr)
    y /= max(1e-9, float(np.max(np.abs(y))))
    y = (0.5 * y * 32767).astype("<i2")
    buf = io.BytesIO()
    with wave.open(buf, "wb") as w:
        w.setnchannels(1)
        w.setsampwidth(2)
        w.setframerate(sr)
        w.writeframes(y.tobytes())
    return buf.getvalue()


def _load_or_synth(path: str, f1: float, f2: float, f3: float) -> bytes:
    if os.path.exists(path):
        with open(path, "rb") as fh:
            return fh.read()
    return _synthesize_vowel_wav(f1, f2, f3)


