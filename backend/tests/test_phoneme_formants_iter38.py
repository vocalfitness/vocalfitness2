class TestConsentEndpointsIndependent:
    def test_get_consent(self, headers):
        r = requests.get(f"{BASE_URL}/api/phonemes/consent", headers=headers,
                         timeout=30)
        assert r.status_code == 200, r.text
        body = r.json()
        assert "audio_granted" in body
        assert "video_granted" in body
        assert isinstance(body["audio_granted"], bool)
        assert isinstance(body["video_granted"], bool)

    def test_set_audio_does_not_affect_video(self, headers):
        # Grant video, revoke audio → video must remain granted
        _grant(headers, "video", True)
        _grant(headers, "audio", False)
        r = requests.get(f"{BASE_URL}/api/phonemes/consent", headers=headers,
                         timeout=30)
        body = r.json()
        assert body["audio_granted"] is False
        assert body["video_granted"] is True
        # And vice versa: grant audio, revoke video → audio remains granted
        _grant(headers, "audio", True)
        _grant(headers, "video", False)
        r = requests.get(f"{BASE_URL}/api/phonemes/consent", headers=headers,
                         timeout=30)
        body = r.json()
        assert body["audio_granted"] is True
        assert body["video_granted"] is False
        # Restore
        _grant(headers, "video", True)

    def test_invalid_kind_400(self, headers):
        r = requests.post(
            f"{BASE_URL}/api/phonemes/consent",
            json={"kind": "notakind", "granted": True},
            headers=headers, timeout=30,
        )
        assert r.status_code == 400, r.text


