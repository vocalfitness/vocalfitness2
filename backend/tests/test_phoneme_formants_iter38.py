def _grant(headers: dict, kind: str, granted: bool):
    r = requests.post(
        f"{BASE_URL}/api/phonemes/consent",
        json={"kind": kind, "granted": granted},
        headers=headers, timeout=30,
    )
    assert r.status_code == 200, r.text
    return r.json()


def _analyze(headers: dict, wav: bytes, phoneme_ipa: str, dialect: str,
             target_kind: str = "phoneme",
             reference_url: str = "") -> requests.Response:
    files = {"file": ("v.wav", wav, "audio/wav")}
    data = {
        "phoneme_ipa": phoneme_ipa,
        "dialect": dialect,
        "target_kind": target_kind,
        "reference_url": reference_url,
    }
    return requests.post(
        f"{BASE_URL}/api/phonemes/analyze-formants",
        headers=headers, files=files, data=data, timeout=60,
    )


